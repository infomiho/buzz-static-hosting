from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path

SCRIPT_DIR = Path(__file__).resolve().parent
REPOSITORY_ROOT = SCRIPT_DIR.parents[2]
SERVER_ROOT = REPOSITORY_ROOT / "server"
sys.path.insert(0, str(SERVER_ROOT / "src"))

os.environ.pop("BUZZ_GSC_CREDENTIALS", None)

from server.app import create_app  # noqa: E402
from server.environment import ENVIRONMENT_VARIABLES, EnvironmentVariable  # noqa: E402

OPENAPI_PATH = SCRIPT_DIR.parent / "public" / "openapi.json"
CONFIGURATION_PATH = (
    SCRIPT_DIR.parent / "src" / "content" / "docs" / "reference" / "configuration.md"
)
ENV_EXAMPLE_PATH = SERVER_ROOT / ".env.example"


def display_default(variable: EnvironmentVariable) -> str:
    if variable.default is None:
        return "Not set"
    if variable.name == "BUZZ_DATA_DIR":
        return "Server package directory"
    if isinstance(variable.default, bool):
        return f"`{str(variable.default).lower()}`"
    if isinstance(variable.default, (frozenset, set)) and not variable.default:
        return "Not set"
    return f"`{variable.default}`"


def render_configuration() -> str:
    rows = []
    for variable in ENVIRONMENT_VARIABLES:
        requirement = variable.required or "Optional"
        scope = "Buzz server" if variable.scope == "server" else "Standalone deployment"
        rows.append(
            f"| `{variable.name}` | {variable.description} | {scope} | "
            f"{display_default(variable)} | {requirement} |"
        )

    return """---
title: Configuration
description: Environment variables used by the Buzz server and bundled deployment.
editUrl: false
---

<!-- Generated from server/src/server/environment.py. Do not edit this file. -->

Buzz reads server configuration when the Python modules are imported. Restart the server after changing an environment variable.

Command-line options take precedence where noted. Values used only by the bundled Traefik deployment are marked as standalone deployment settings.

## Environment Variables

| Variable | Purpose | Scope | Default | Requirement |
| --- | --- | --- | --- | --- |
""" + "\n".join(rows) + """

## CLI Environment Variables

The CLI reads `BUZZ_SERVER` and `BUZZ_TOKEN`. The root `--server` and `--token` options take precedence, followed by environment variables, then saved configuration.

Keep secrets outside source control. `GITHUB_CLIENT_SECRET`, `BUZZ_ANALYTICS_SECRET`, `BUZZ_GSC_CREDENTIALS`, `BUZZ_TRAEFIK_CONTROL_TOKEN`, `BUZZ_TRAEFIK_API_AUTHORIZATION`, `CF_API_TOKEN`, and `BUZZ_TOKEN` contain sensitive values.
"""


