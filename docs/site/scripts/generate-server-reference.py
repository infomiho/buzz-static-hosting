def canonical_openapi() -> str:
    return json.dumps(
        create_app().openapi(),
        sort_keys=True,
        indent=2,
        ensure_ascii=False,
    ) + "\n"


def write_or_check(path: Path, content: str, check: bool) -> None:
    if check:
        if not path.exists() or path.read_text() != content:
            raise SystemExit(f"Generated file is stale: {path.relative_to(REPOSITORY_ROOT)}")
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content)


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate Buzz server reference files")
    parser.add_argument("--check", action="store_true")
    args = parser.parse_args()

    write_or_check(OPENAPI_PATH, canonical_openapi(), args.check)
    write_or_check(CONFIGURATION_PATH, render_configuration(), args.check)
    write_or_check(ENV_EXAMPLE_PATH, render_env_example(), args.check)


if __name__ == "__main__":
    main()
